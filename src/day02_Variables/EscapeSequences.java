package day02_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello");
        System.out.println("\t Hello");
        System.out.println("\t \t\t\t\t\t\tHello");

          //\t means double pharagrap space
        System.out.println("Cybertek School");
        System.out.println("Cybertek \nSchool");
        System.out.println("\n\n\nMy \nName \nIs \nLale");
        //\n means new line, starts with new line
        //print my favorite tv show is "Game of Thrones"
        System.out.println("My favorite tv show is \"Game of Thrones\"");
        System.out.println("My favorite actor is \"Angelina Jolie\"");
        //\" means double quote, prints double quote on the consule
        System.out.println("\\");
        // prints sigle \ slash on consule
        System.out.println("/");
        System.out.println("My favorite book is 'java'");    //' prints the '
        System.out.println("My favorite book is \'java\'"); //\' prints the '



    }

}
